//Computer Programming Week 2 Example 3
//Show number in different ways

#include <iostream>
using namespace std;

int main(void)
{
    cout << 3.14159 << "\n" << 3.0 << "\n";
    cout << fixed <<    3.14159  << "\n" << 3.0 << "\n";
    cout << scientific << 3.14159 << "\n" << 3.0 << "\n";

    return 0;
}